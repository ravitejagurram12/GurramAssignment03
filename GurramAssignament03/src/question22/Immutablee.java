package question22;
public final class Immutablee {
	  private String name;
	  private int age;

	  public Immutablee(String name, int age) {
	    this.name = name;
	    this.age = age;
	  }

	  // getter method
	  public String getName() {
	    return name;
	  }
	  public int getAge() {
	    return age;
	  }

}